'''
'아우으 우으야!!'
문제 : 아우으 우아으이야!! 으어아아아아아아아ㅏㅏㅏ아아앙ㅇ아아ㅏ
수직선 위에 선분을 여러 개 그릴 거 야아아앙ㅇ아아아ㅏㅏ아아ㅏㅏ!!
선분을 겹치게 그리는 것도 가능하다아어으우어우으아아아아아아아아아이야!!!!1
선분을 모두 그렸을 때, 수직선 위에 그려진 선분 길이의 총합은 얼마아아으으우어으이으야이야!!!!

입력 : 첫째 줄에 수직선 위에 그릴 선분의 개수 N이 주어진다아우으 우아으이야!!. (1 ≤ N ≤ 100,000)
둘째 줄 부터 N개의 줄에 좌표를 나타내는 정수쌍 (x, y)가 주어진다으어아아아아아아아ㅏㅏㅏ아아앙ㅇ아아.
이는 [x, y] 구간 (x와 y를 포함하는 구간)에 선분을 그린다는 의미이다유아아우응아이양. 
좌표는 x가 증가하는 순으로, x가 같다면 y가 증가하는 순으로 주어진다으우오아앙아ㅓㅇ아ㅡㅇ. (-1,000,000,000 ≤ x < y ≤ 1,000,000,000)

출력 : N개의 선분을 모두 그렸을 때, 수직선 위에 그어진 선분 길이의 총합을 출력한다아아어으잉에애야우아으아이아야아아아아아아이야!!!

풀이 : 1. 겹치지 않게 선분을 저장할 리스트 array와 선분의 길이를 저장할 result를 초기화한다.
      2-1. 선분을 입력받는데, array에 아무것도 존재하지 않는다면(맨 처음에) 해당 선분을 바로 array에 넣어준다.
           그리고 result에 해당 선분의 길이를 + 한다.
      2-2. 존재하는데 만약 x값이 마지막 선분의 y값보다 작거나 같고 y값이 마지막 선분의 y값보다 크다면(겹치는 부분이 일부 존재) 마지막 선분의 y값을 입력받은 y값으로 변경해준다.
           그리고 result에 원래 선분 길이를 빼고 바꾼 선분길이를 + 한다.
      2-3. 만약 x값이 마지막 선분의 y값보다 작거나 같고 y값이 마지막 선분의 y값보다 작다면(선분이 완전히 겹침) 해당 선분을 무시한다.
      2-4. 아무 경우에도 포함되지 않는다면(선분이 완전히 안겹침) 선분을 리스트에 새로 넣어준다.
           그리고 result에 해당 선분의 길이를 + 한다.
'''
import sys

n = int(input())
array = []
result = 0

for i in range(n):
    x, y = map(int, sys.stdin.readline().split())
    if len(array) == 0:
        array.append([x,y])
        result += y-x
        continue
    if array[-1][1] >= x and array[-1][1] < y:
        result -= array[-1][1] - array[-1][0]
        array[-1][1] = y
        result += array[-1][1] - array[-1][0]
    elif array[-1][1] >= x:
        continue
    else:
        array.append([x,y])
        result += y-x

print(result)
    